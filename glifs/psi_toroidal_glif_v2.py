def char_weight(ch: str) -> float:
    return DENSITY.get(ch, 0.45)

def lines() -> list[str]:
    return ASCII_GLIF.splitlines()

def size() -> tuple[int, int]:
    L = lines()
    h = len(L)
    w = max(len(s) for s in L) if L else 0
    return w, h

def histogram() -> Counter:
    c = Counter()
    for row in lines():
        c.update(row)
    return c

def fill_ratio() -> float:
    c = histogram()
    filled = sum(v for k, v in c.items() if k != " ")
    total = sum(c.values())
    return 0.0 if total == 0 else filled / total

def symmetry_lr() -> float:
    L = lines()
    if not L:
        return 1.0
    w, h = size()
    score, count = 0.0, 0
    for row in L:
        row = row.ljust(w)
        for i in range(w // 2):
            a, b = row[i], row[w - 1 - i]
            score += 1.0 if a == b else 0.0
            count += 1
    return score / max(count, 1)

def symmetry_tb() -> float:
    L = lines()
    if not L:
        return 1.0
    w, h = size()
    score, count = 0.0, 0
    for j in range(h // 2):
        a, b = L[j].ljust(w), L[h - 1 - j].ljust(w)
        for i in range(w):
            score += 1.0 if a[i] == b[i] else 0.0
            count += 1
    return score / max(count, 1)

def radial_profile(bins: int = 24) -> list[float]:
    L = lines()
    if not L:
        return []
    w, h = size()
    cx, cy = (w - 1) / 2.0, (h - 1) / 2.0
    rmax = math.hypot(max(cx, w - 1 - cx), max(cy, h - 1 - cy))
    sums = [0.0] * bins
    cnts = [0] * bins
    for y, row in enumerate(L):
        row = row.ljust(w)
        for x, ch in enumerate(row):
            r = math.hypot(x - cx, y - cy)
            b = min(bins - 1, int((r / (rmax + 1e-9)) * bins))
            sums[b] += char_weight(ch)
            cnts[b] += 1
    return [(s / c if c else 0.0) for s, c in zip(sums, cnts)]

def save_snapshot(out_dir: Path) -> Path:
    out_dir.mkdir(parents=True, exist_ok=True)
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    p = out_dir / f"psi_toroidal_glif_v2_{ts}.txt"
    p.write_text(ASCII_GLIF, encoding="utf-8")
    return p

def main():
    print(ASCII_GLIF)

    w, h = size()
    fr = fill_ratio()
    sLR = symmetry_lr()
    sTB = symmetry_tb()
    hist = histogram().most_common(8)
    prof = radial_profile(16)

    print("\n— METRICS —")
    print(f"size: {w}×{h} | fill_ratio: {fr:.3f} | symmetry(LR/TB): {sLR:.3f}/{sTB:.3f}")
    print("top chars:", ", ".join([f"{k}:{v}" for k, v in hist]))
    print("radial_profile (16 bins):", ", ".join(f"{v:.2f}" for v in prof))

    target = Path.home() / "psi_lab" / "glifs"
    path = save_snapshot(target)
    print(f"saved: {path}")

